#include "main.h"

/**
 * add - adds two integers.
 * @a: first integer
 * @b: second integer
 * Return: Sum of two integers.
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - finds difference between two integers
 * @a: first integer
 * @b: second integer
 * Return: Difference.
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - finds product of two integers
 * @a: first integer
 * @b: second integer
 * Return: Product.
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - finds the quotient of two integers
 * @a: first integer
 * @b: second integer
 * Return: quotient.
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - finds modulus of two integers
 * @a: first integer
 * @b: second integer
 * Return: modulus.
 */
int mod(int a, int b)
{
	return (a % b);
}
